#define FONT_CHAR_WIDTH 6
#define FONT_CHAR_HEIGHT 8
#define FONT_FIRST_CHAR 0x20
#define FONT_LAST_CHAR 0x7E
#define FONT_BITMAP_COLS 5
#define FONT_SCALE 20
static inline void draw_pixel(renderer_context_t *context, int px, int py, color_t color)
{
  if (px < 0 || py < 0)
    return;
  if ((uint32_t)px >= context->width || (uint32_t)py >= context->height)
    return;

  context->pixels[py * context->width + px] = color;
}

static void draw_char(renderer_context_t *context, int px, int py, char c, color_t color)
{
  if (c < FONT_FIRST_CHAR || c > FONT_LAST_CHAR)
    c = '?';

  const uint8_t *glyph = char_to_bitmap[(uint8_t)c - FONT_FIRST_CHAR];

  for (int row = 0; row < FONT_CHAR_HEIGHT; row++)
  {
    uint8_t bits = glyph[row];
    for (int col = 0; col < FONT_BITMAP_COLS; col++)
    {
      if (bits & (1 << (FONT_BITMAP_COLS - 1 - col)))
      {
        for (int dy = 0; dy < FONT_SCALE; dy++)
          for (int dx = 0; dx < FONT_SCALE; dx++)
            draw_pixel(context, px + col * FONT_SCALE + dx, py + row * FONT_SCALE + dy, color);
      }
    }
  }
}

void renderer_draw_text(renderer_context_t *context, float x, float y, const char *text, color_t color)
{
  if (!context || !text)
    return;

  int origin_x = (int)x;
  int cursor_x = origin_x;
  int cursor_y = (int)y;

  for (const char *p = text; *p != '\0'; p++)
  {
    char c = *p;

    if (c == '\n')
    {
      cursor_x = origin_x;
      cursor_y += FONT_CHAR_HEIGHT * FONT_SCALE;
      continue;
    }

    if (c == '\t')
    {
      cursor_x += FONT_CHAR_WIDTH * 4 * FONT_SCALE;
      continue;
    }

    if (c < FONT_FIRST_CHAR || c > FONT_LAST_CHAR)
    {
      cursor_x += FONT_CHAR_WIDTH * FONT_SCALE;
      continue;
    }

    draw_char(context, cursor_x, cursor_y, c, color);
    cursor_x += FONT_CHAR_WIDTH * FONT_SCALE;
  }
}
