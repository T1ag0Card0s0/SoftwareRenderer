static void draw_char(framebuffer_t *framebuffer, int px, int py, char c, const text_style_t *style)
{
  const uint8_t *bitmap = char_to_bitmap[c - BASE_ASCII_TABLE_INDEX];

  for (int row = 0; row < BITMAP_HEIGHT; row++)
  {
    for (int col = 0; col < BITMAP_WIDTH; col++)
    {
      uint8_t bit = (bitmap[row] >> col) & 1;
      int draw_x = px + col * style->scale;
      int draw_y = py + row * style->scale;

      if (bit)
      {
        framebuffer_clean(framebuffer,
                          (rect_t){.x = draw_x, .y = draw_y, .width = style->scale, .height = style->scale}, style->fg);
      }
      else if (style->draw_bg)
      {
        framebuffer_clean(framebuffer,
                          (rect_t){.x = draw_x, .y = draw_y, .width = style->scale, .height = style->scale}, style->bg);
      }
    }
  }
}

void text_draw(framebuffer_t *framebuffer, int x, int y, const char *text, const text_style_t *style)
{
  int cursor_x = x;
  int cursor_y = y;

  for (int i = 0; text[i] != '\0'; i++)
  {
    switch (text[i])
    {
    case '\n':
      cursor_y += BITMAP_HEIGHT * style->scale;
      cursor_x = x;
      break;

    case '\r':
      cursor_x = x;
      break;

    default:
      draw_char(framebuffer, cursor_x, cursor_y, text[i], style);
      cursor_x += BITMAP_WIDTH * style->scale;
      break;
    }
  }
}
